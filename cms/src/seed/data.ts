/** Convert plain paragraphs into Strapi blocks format. */
// eslint-disable-next-line @typescript-eslint/no-explicit-any
export function toBlocks(paragraphs: string[]): any {
  return paragraphs.map((p) => ({
    type: "paragraph",
    children: [{ type: "text", text: p }],
  }));
}
